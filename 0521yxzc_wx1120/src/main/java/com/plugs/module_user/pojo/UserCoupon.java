package com.plugs.module_user.pojo;

/**
 * Created by Administrator on 2016/11/2.
 * 个人账户优惠券查询映射实体
 */
public class UserCoupon {

    private String sysCouponUuid;    //优惠券id
    private String userUuid;         //用户id
    private String serialNum;        //优惠券子序号（8位，从1开始顺序）
    private int status;              //状态 0：无效，1：有效，2：已使用，3：被占用，待支付成功后变为[2:已使用]
    private String orderUuid;        //订单id
    private String name;             //名称
    private int type;                //类型1打折，2金额
    private double money;            //金额
    private String useCondition;     //使用条件
    private double astrict;          //条件金额
    private double discount;     //折扣

    public double getDiscount() {
        return discount;
    }

    public void setDiscount(double discount) {
        this.discount = discount;
    }

    public String getSysCouponUuid() {
        return sysCouponUuid;
    }

    public void setSysCouponUuid(String sysCouponUuid) {
        this.sysCouponUuid = sysCouponUuid;
    }

    public String getUserUuid() {
        return userUuid;
    }

    public void setUserUuid(String userUuid) {
        this.userUuid = userUuid;
    }

    public String getSerialNum() {
        return serialNum;
    }

    public void setSerialNum(String serialNum) {
        this.serialNum = serialNum;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getOrderUuid() {
        return orderUuid;
    }

    public void setOrderUuid(String orderUuid) {
        this.orderUuid = orderUuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

    public String getUseCondition() {
        return useCondition;
    }

    public void setUseCondition(String useCondition) {
        this.useCondition = useCondition;
    }

    public double getAstrict() {
        return astrict;
    }

    public void setAstrict(double astrict) {
        this.astrict = astrict;
    }
}
