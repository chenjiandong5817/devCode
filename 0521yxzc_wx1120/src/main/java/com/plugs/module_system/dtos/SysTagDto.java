package com.plugs.module_system.dtos;

import java.util.Date;

/**
 * 标签实体
 *
 * @author Zoro
 * @since 2016/9/28
 */
public class SysTagDto {
    private String uuid;        //主键
    private String tagName;        //标签内容
    private int star;
    private int tagType;        //标签类型 1.评价（这里标签是通用的，可以用在评价，也可以用在可能拓展的外表）
    private int tagStatus;        //标签状态（1.启用  2.未启用）
    private Date createTime;        //创建时间
    private String creator;        //创建人

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getUuid() {
        return uuid;
    }

    public void setTagName(String tagName) {
        this.tagName = tagName;
    }

    public String getTagName() {
        return tagName;
    }

    public int getStar() { return star; }

    public void setStar(int star) { this.star = star; }

    public void setTagType(int tagType) {
        this.tagType = tagType;
    }

    public int getTagType() {
        return tagType;
    }

    public void setTagStatus(int tagStatus) {
        this.tagStatus = tagStatus;
    }

    public int getTagStatus() {
        return tagStatus;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public String getCreator() {
        return creator;
    }

}
